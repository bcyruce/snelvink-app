type LanguageContextValue = {
  language: Language;
  setLanguage: (language: Language) => void;
  t: (key: TranslationKey, vars?: Record<string, string | number>) => string;
  translateHaccpText: (input: string) => string;
};

const LanguageContext = createContext<LanguageContextValue | undefined>(undefined);

function isSupportedLanguage(value: string | null | undefined): value is Language {
  return (
    value === "nl" ||
    value === "en" ||
    value === "zh" ||
    value === "ar" ||
    value === "tr"
  );
}

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguageState] = useState<Language>(() => {
    try {
      if (typeof window === "undefined") return "nl";
      const stored = window.localStorage.getItem(STORAGE_KEY);
      if (isSupportedLanguage(stored)) {
        return stored;
      }
    } catch {
      // ignore storage read failures
    }
    return "nl";
  });

  // Keep <html lang> and <html dir> in sync with the current language.
  // Required for screen readers, font selection, and proper RTL layout (Arabic).
  useEffect(() => {
    if (typeof document === "undefined") return;
    const meta = LANGUAGE_META[language];
    document.documentElement.lang = meta.htmlLang;
    document.documentElement.dir = meta.dir;
  }, [language]);

  const setLanguage = (nextLanguage: Language) => {
    setLanguageState(nextLanguage);
    try {
      window.localStorage.setItem(STORAGE_KEY, nextLanguage);
    } catch {
      // ignore storage write failures
    }
  };

  const value = useMemo<LanguageContextValue>(() => {
    const t = (key: TranslationKey, vars?: Record<string, string | number>) => {
      let template: string =
        translations[language][key] ?? translations.nl[key] ?? key;
      if (!vars) return template;
      Object.entries(vars).forEach(([varKey, varValue]) => {
        template = template.replace(`{${varKey}}`, String(varValue));
      });
      return template;
    };

    const translateHaccpText = (input: string) => {
      let output = input;
      Object.entries(termAliases).forEach(([alias, key]) => {
        const re = new RegExp(`\\b${alias}\\b`, "gi");
        output = output.replace(re, t(key));
      });
      return output;
    };

    return {
      language,
      setLanguage,
      t,
      translateHaccpText,
    };
  }, [language]);

  return <LanguageContext.Provider value={value}>{children}</LanguageContext.Provider>;
}

export function useLanguageContext() {
  const ctx = useContext(LanguageContext);
  if (!ctx) {
    throw new Error("useLanguageContext must be used inside LanguageProvider");
  }
  return ctx;
}
